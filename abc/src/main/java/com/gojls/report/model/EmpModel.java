package com.gojls.report.model;


public class EmpModel {
	private String empSeq;
	private String empId;
	private String empNm;
	private String empPw;
	private String useFg;
	private String empTypeCd;
	private String empRole;
	
	public String getEmpSeq() {
		return empSeq;
	}
	public void setEmpSeq(String empSeq) {
		this.empSeq = empSeq;
	}
	public String getEmpId() {
		return empId;
	}
	public void setEmpId(String empId) {
		this.empId = empId;
	}
	public String getEmpNm() {
		return empNm;
	}
	public void setEmpNm(String empNm) {
		this.empNm = empNm;
	}
	public String getEmpPw() {
		return empPw;
	}
	public void setEmpPw(String empPw) {
		this.empPw = empPw;
	}
	public String getUseFg() {
		return useFg;
	}
	public void setUseFg(String useFg) {
		this.useFg = useFg;
	}
	public String getEmpTypeCd() {
		return empTypeCd;
	}
	public void setEmpTypeCd(String empTypeCd) {
		this.empTypeCd = empTypeCd;
	}
	public String getEmpRole() {
		return empRole;
	}
	public void setEmpRole(String empRole) {
		this.empRole = empRole;
	}

	
}
